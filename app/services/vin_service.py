class VinDecoder:
    """VIN码解码器：校验、解析 WMI/VDS/VIS、识别品牌和年份。"""

    def decode(self, vin: str) -> Dict:
        """
        完整解码 VIN。

        返回:
            {
                "valid": bool,         # 校验是否通过
                "vin": str,
                "wmi": dict,           # 世界制造商识别
                "vds": dict,           # 车辆描述段
                "vis": dict,           # 车辆识别段
                "full_name": str,      # 完整名称
                "manufacturer": str,   # 制造商
                "brand": str,          # 品牌
                "year": int|None,      # 车型年份
                "country": str,        # 生产国
                "check_digit_valid": bool, # 第9位校验是否通过
                "error": str|None      # 错误信息
            }
        """
        vin = vin.strip().upper()
        result: Dict = {
            "valid": False,
            "vin": vin,
            "wmi": {},
            "vds": {},
            "vis": {},
            "full_name": "",
            "manufacturer": "",
            "brand": "",
            "year": None,
            "country": "",
            "check_digit_valid": False,
            "error": None,
        }

        # 1. 基本格式校验
        if not vin:
            result["error"] = "VIN 为空"
            return result
        if len(vin) != 17:
            result["error"] = f"VIN 长度应为17位，当前为 {len(vin)} 位"
            return result
        if not _VIN_PATTERN.match(vin):
            result["error"] = "VIN 包含非法字符（不允许 I, O, Q）"
            return result

        # 2. 校验码验证
        result["check_digit_valid"] = self._validate(vin)

        # 3. 分段解析
        result["wmi"] = self._parse_wmi(vin)
        result["vds"] = self._parse_vds(vin)
        result["vis"] = self._parse_vis(vin)

        # 4. 品牌信息
        wmi = vin[:3]
        info = _WMI_TABLE.get(wmi)
        if info:
            manufacturer, brand, country = info
            result["manufacturer"] = manufacturer
            result["brand"] = brand
            result["country"] = country
            result["full_name"] = f"{manufacturer} {brand}"
        else:
            wmi2 = vin[:2]
            # 尝试通过前2位匹配区域
            region_map = {
                "L": ("中国", "中国"),
                "W": ("德国", "德国"),
                "J": ("日本", "日本"),
                "1": ("美国", "美国"),
                "2": ("加拿大", "加拿大"),
                "3": ("墨西哥", "墨西哥"),
                "4": ("美国", "美国"),
                "5": ("美国", "美国"),
                "K": ("韩国", "韩国"),
                "S": ("英国", "英国"),
                "Z": ("意大利", "意大利"),
                "Y": ("瑞典", "瑞典"),
                "V": ("法国/西班牙", "法国/西班牙"),
                "X": ("荷兰", "荷兰"),
                "T": ("捷克", "捷克"),
                "M": ("印度", "印度"),
                "6": ("澳大利亚", "澳大利亚"),
                "7": ("新西兰", "新西兰"),
                "8": ("阿根廷", "阿根廷"),
                "9": ("巴西", "巴西"),
            }
            country_hint = region_map.get(wmi[:1], ("未知", "未知"))
            result["country"] = country_hint[0]
            result["full_name"] = f"未识别品牌 (WMI: {wmi})"

        # 5. 年份
        result["year"] = result["vis"].get("year")

        # 6. 总体有效性（格式正确即可，校验码仅做参考）
        result["valid"] = True

        return result

    # ------------------------------------------------------------------
    # 内部方法
    # ------------------------------------------------------------------

    def _validate(self, vin: str) -> bool:
        """
        校验 VIN 第9位（校验码）。
        返回 True 表示校验通过或校验码无需验证（非标准情景不拦）。
        """
        if len(vin) != 17:
            return False
        expected = _compute_check_digit(vin)
        actual = vin[8]
        return actual == expected

    def _parse_wmi(self, vin: str) -> Dict:
        """
        解析 WMI (World Manufacturer Identifier) —— 第1-3位。

        返回:
            {
                "code": str,        # 3位 WMI 码
                "country": str,     # 生产国
                "manufacturer": str, # 制造商
                "brand": str,       # 品牌
            }
        """
        wmi = vin[:3]
        info = _WMI_TABLE.get(wmi)
        if info:
            manufacturer, brand, country = info
            return {
                "code": wmi,
                "country": country,
                "manufacturer": manufacturer,
                "brand": brand,
            }
        return {
            "code": wmi,
            "country": _wmi_country_hint(wmi),
            "manufacturer": "未知",
            "brand": "未知",
        }

    def _parse_vds(self, vin: str) -> Dict:
        """
        解析 VDS (Vehicle Descriptor Section) —— 第4-9位。
        包含车型、车身类型、发动机、约束系统等。

        注意：VDS 编码各厂商自定义，这里只做基础分类提取。
        返回:
            {
                "code": str,
                "model_code": str,       # 第4-8位（车型代码）
                "check_digit": str,      # 第9位
                "check_digit_valid": bool,
            }
        """
        vds = vin[3:9]  # 位4-9（0-indexed 3-8）
        check_digit = vin[8] if len(vin) > 8 else ""
        return {
            "code": vds,
            "model_code": vds[:5],  # 第4-8位
            "check_digit": check_digit,
            "check_digit_valid": self._validate(vin) if len(vin) == 17 else False,
        }

    def _parse_vis(self, vin: str) -> Dict:
        """
        解析 VIS (Vehicle Identifier Section) —— 第10-17位。
        包含年份、工厂代码、序列号。

        返回:
            {
                "code": str,
                "year_code": str,       # 第10位
                "year": int|None,       # 解析出的年份
                "plant_code": str,      # 第11位（工厂代码）
                "serial_number": str,   # 第12-17位（生产序列号）
            }
        """
        vis = vin[9:]  # 位10-17
        year_code = vin[9] if len(vin) > 9 else ""
        plant_code = vin[10] if len(vin) > 10 else ""
        serial = vin[11:17] if len(vin) > 16 else ""

        return {
            "code": vis,
            "year_code": year_code,
            "year": _decode_year(vin),
            "plant_code": plant_code,
            "serial_number": serial,
        }


