def decode_vin(vin: str) -> Dict:
    """便捷函数：直接解码 VIN。"""
    return VinDecoder().decode(vin)


def validate_vin(vin: str) -> bool:
    """便捷函数：仅校验 VIN 是否有效（格式 + 校验码）。"""
    return VinDecoder().decode(vin)["valid"]


def get_vin_info(vin: str) -> Optional[Dict]:
    """便捷函数：获取可读信息摘要，无效返回 None。"""
    result = VinDecoder().decode(vin)
    if not result["valid"]:
        return None
    return {
        "vin": result["vin"],
        "brand": result["brand"],
        "model_code": result["vds"]["model_code"],
        "year": result["year"],
        "manufacturer": result["manufacturer"],
        "country": result["country"],
        "full_name": result["full_name"],
    }
