# VIN 正则
_VIN_PATTERN = re.compile(r"^[A-HJ-NPR-Z0-9]{17}$")


# ===================================================================
# 工具函数
# ===================================================================

def _char_value(c: str) -> int:
    """返回 VIN 字符对应的数值。"""
    return _VIN_CHAR_MAP.get(c.upper(), 0)


def _compute_check_digit(vin: str) -> str:
    """计算 VIN 第9位校验码。"""
    total = sum(
        _char_value(vin[i]) * _VIN_WEIGHTS[i]
        for i in range(17)
    )
    remainder = total % 11
    return "X" if remainder == 10 else str(remainder)


def _decode_year(vin: str) -> Optional[int]:
    """从 VIN 第10位解码车型年份。"""
    code = vin[9].upper() if len(vin) > 9 else ""
    return _YEAR_CODES.get(code) if code else None


