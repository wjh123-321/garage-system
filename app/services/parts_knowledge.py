def get_brands() -> list:
    """返回所有支持的品牌列表"""
    return sorted(PARTS_DATABASE.keys())


def get_models(brand: str) -> list:
    """返回指定品牌下的车型列表"""
    brand = _fuzzy_match_brand(brand)
    if brand not in PARTS_DATABASE:
        return []
    return sorted(PARTS_DATABASE[brand].keys())


def get_part_detail(brand: str, model: str, part_name: str) -> dict:
    """返回单个配件详情"""
    brand = _fuzzy_match_brand(brand)
    model = _fuzzy_match_model(brand, model)

    if brand not in PARTS_DATABASE:
        return {"error": f"不支持品牌: {brand}"}
    if model not in PARTS_DATABASE[brand]:
        return {"error": f"车型 '{model}' 在 {brand} 下不存在"}
    if part_name not in PARTS_DATABASE[brand][model]["parts"]:
        return {"error": f"配件 '{part_name}' 在 {brand} {model} 中不存在"}

    part = PARTS_DATABASE[brand][model]["parts"][part_name]
    return {
        "brand": brand,
        "model": model,
        "part_name": part_name,
        "oe": part["oe"],
        "brands": part["brands"],
        "price": part["price"],
        "unit": part["unit"],
        "qty": part.get("qty", 1),
        "years": PARTS_DATABASE[brand][model]["years"],
        "engine": PARTS_DATABASE[brand][model]["engine"],
    }


def query_parts(brand: str = "", model: str = "", keyword: str = "") -> dict:
    """查询配件，返回匹配的车型和配件列表

    支持模糊匹配品牌和车型，支持按配件名称关键词筛选。
    参数留空表示不限制。

    返回格式:
    {
        "total": 3,
        "results": [
            {
                "brand": "大众",
                "model": "朗逸",
                "years": "2019-2023",
                "engine": ["1.4T", "2.0T"],
                "parts": { ... }
            },
            ...
        ]
    }
    """
    results = []

    for brand_name, brand_models in PARTS_DATABASE.items():
        if brand and brand.lower() not in brand_name.lower():
            continue

        for model_name, model_data in brand_models.items():
            if model and model.lower() not in model_name.lower():
                continue

            parts = model_data["parts"]
            if keyword:
                filtered_parts = {}
                for part_name, part_info in parts.items():
                    if keyword.lower() in part_name.lower():
                        filtered_parts[part_name] = part_info
                if not filtered_parts:
                    continue
                matched_parts = filtered_parts
            else:
                matched_parts = parts

            results.append({
                "brand": brand_name,
                "model": model_name,
                "years": model_data["years"],
                "engine": model_data["engine"],
                "parts": matched_parts,
            })

    return {
        "total": len(results),
        "results": results,
    }


def _fuzzy_match_brand(brand: str) -> str:
    """模糊匹配品牌名，支持常用简称"""
    if not brand:
        return brand

    brand = brand.strip()

    # 精确匹配
    if brand in PARTS_DATABASE:
        return brand

    # 别名映射
    alias_map = {
        "vw": "大众",
        "volkswagen": "大众",
        "toyota": "丰田",
        "honda": "本田",
        "nissan": "日产",
        "byd": "比亚迪",
        "benz": "奔驰",
        "mercedes": "奔驰",
        "mercedes-benz": "奔驰",
        "bmw": "宝马",
        "audi": "奥迪",
        "大众汽车": "大众",
        "北京奔驰": "奔驰",
        "华晨宝马": "宝马",
        "一汽奥迪": "奥迪",
        "一汽大众": "大众",
        "上汽大众": "大众",
    }

    lower = brand.lower()
    for alias, standard in alias_map.items():
        if lower == alias or lower == alias.lower():
            return standard

    # 模糊匹配
    for key in PARTS_DATABASE:
        if brand.lower() in key.lower():
            return key

    return brand


def _fuzzy_match_model(brand: str, model: str) -> str:
    """模糊匹配车型名"""
    if not model or brand not in PARTS_DATABASE:
        return model

    model = model.strip()

    if model in PARTS_DATABASE[brand]:
        return model

    # 别名映射
    model_alias = {
        "rav4": "RAV4荣放",
        "荣放": "RAV4荣放",
        "秦": "秦PLUS",
        "宋": "宋PLUS",
        "c级": "C级",
        "e级": "E级",
        "3系": "3系",
        "5系": "5系",
        "a4": "A4L",
        "a6": "A6L",
        "q5": "Q5",
    }

    for alias, standard in model_alias.items():
        if model.lower() == alias.lower() or model.lower() in alias.lower():
            if standard in PARTS_DATABASE[brand]:
                return standard

    return model


def get_total_parts_count() -> dict:
    """统计数据库中配件总数（品牌/车型/配件三级统计）"""
    counts = {}
    total_models = 0
    total_parts = 0

    for brand, models in PARTS_DATABASE.items():
        model_count = len(models)
        parts_count = sum(len(m["parts"]) for m in models.values())
        counts[brand] = {
            "models": model_count,
            "parts": parts_count,
        }
        total_models += model_count
        total_parts += parts_count

    return {
        "brands": len(PARTS_DATABASE),
        "total_models": total_models,
        "total_parts": total_parts,
        "detail": counts,
    }


